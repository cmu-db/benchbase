DROP TABLE IF EXISTS A CASCADE;
DROP TABLE IF EXISTS B CASCADE;
DROP TABLE IF EXISTS C CASCADE;


CREATE TABLE a (
    a_IATTR int NOT NULL,
    PRIMARY KEY (A_IATTR)
);

CREATE TABLE b (
    b_IATTR int NOT NULL,
    PRIMARY KEY (B_IATTR)
);

CREATE TABLE c (
    c_key char(55) NOT NULL,
    a_IATTR int NOT NULL,
    b_IATTR int NOT NULL,
    FOREIGN KEY (a_IATTR) REFERENCES A(a_IATTR) ON DELETE CASCADE,
    FOREIGN KEY (b_IATTR) REFERENCES B(b_IATTR) ON DELETE CASCADE,
    PRIMARY KEY (c_key)
);
